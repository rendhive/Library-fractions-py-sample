from fractions import Fraction

a = Fraction(1, 2)
b = Fraction(1, 4)
result = (a + b) * Fraction(2, 1)  # Menggunakan pecahan dalam operasi campuran
print("Hasil (1/2 + 1/4) * 2:", result)
# Fungsi: Menggunakan pecahan dalam operasi campuran.
# Kondisi: Saat Anda melakukan perhitungan kompleks dengan pecahan.
